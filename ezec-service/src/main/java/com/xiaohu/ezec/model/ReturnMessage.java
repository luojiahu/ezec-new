package com.xiaohu.ezec.model;

public class ReturnMessage {
    
    // 返回码
    private String code;
    
    // 返回描述
    private String desc;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
